<template>
    <div class="user bg-white p-3 m-3">
        <el-row>
            <el-form ref="form" :model="form" label-width="80px">
                <form-query @search="search" @reset="reset">
                    <template slot="show">
                        <el-col :md="6" :sm="8">
                            <el-form-item label="账号:" prop="username">
                                <el-input v-model="form.username" clearable></el-input>
                            </el-form-item>
                        </el-col>
                        <el-col :md="6" :sm="8">
                            <el-form-item label="性别:" prop="sex">
                                <el-select v-model="form.sex" clearable filterable class="w-full">
                                    <template v-for="item in sex">
                                        <el-option :value="item.itemValue" :label="item.itemText"></el-option>
                                    </template>
                                </el-select>
                            </el-form-item>
                        </el-col>
                    </template>
                    <template slot="hide">
                        <el-col :md="6" :sm="8">
                            <el-form-item label="邮箱:" prop="email">
                                <el-input v-model="form.email" clearable></el-input>
                            </el-form-item>
                        </el-col>
                        <el-col :md="6" :sm="8">
                            <el-form-item label="手机号码:" prop="phone">
                                <el-input v-model="form.phone" clearable></el-input>
                            </el-form-item>
                        </el-col>
                        <el-col :md="6" :sm="8">
                            <el-form-item label="状态:" prop="status">
                                <el-select v-model="form.status" clearable filterable class="w-full">
                                    <template v-for="item in userStatus">
                                        <el-option :value="item.itemValue" :label="item.itemText"></el-option>
                                    </template>
                                </el-select>
                            </el-form-item>
                        </el-col>
                    </template>
                </form-query>
            </el-form>
        </el-row>
        <el-row class="my-3">
            <el-button plain type="primary" icon="el-icon-plus" @click="addUser" v-has="'user:add'">添加用户</el-button>
            <el-button plain icon="iconfont icon-wy-upload" v-has="'user:import'" @click="fileImport">
                {{$t('common_import')}}
            </el-button>
            <el-button plain icon="iconfont icon-wy-download" @click="fileExport">{{$t('common_export')}}</el-button>
            <el-dropdown placement="bottom" class="dropdown" v-show="show.batch">
                <el-button plain>
                    批量操作<i class="el-icon-arrow-down el-icon--right"></i>
                </el-button>
                <el-dropdown-menu slot="dropdown">
                    <el-dropdown-item @click.native="deleteBatch"><i class="el-icon-delete"></i>删除</el-dropdown-item>
                    <el-dropdown-item @click.native="frozen()"><i class="el-icon-lock"></i>冻结</el-dropdown-item>
                    <el-dropdown-item @click.native="frozen(1)"><i class="el-icon-unlock"></i>解冻</el-dropdown-item>
                </el-dropdown-menu>
            </el-dropdown>
        </el-row>
        <el-row>
            <el-table-bar>
                <fox-table
                        border
                        stripe
                        fit
                        align="center"
                        v-loading="table.loading"
                        :column="table.column"
                        :data="table.data"
                        pagination
                        background
                        layout="total, sizes, prev, pager, next, jumper"
                        :page-sizes="[5, 10, 20, 30]"
                        :page-count="5"
                        :current-page.sync="page.currentPage"
                        :total="page.total"
                        :page-size="page.pageSize"
                        @size-change="handleSizeChange"
                        @p-current-change="handleCurrentChange"
                        @selection-change="checked"
                ></fox-table>
            </el-table-bar>
        </el-row>
        <drag-drawer v-model="drawer.show"
                     :draggable="drawer.draggable"
                     :title="drawer.title"
                     :width.sync="drawer.width"
                     :direction="drawer.direction"
                     :scrollable="true"
        >

            <component :ref="component.ref" :is="component.type" :data="component.data"
                       @closeFlush="closeFlush"></component>
            <div class="dialog-footer p-2 w-full" v-show = "drawer.showFooter">
                <div class="flex justify-end">
                    <popover-confirm @confirm="popoverConfirm" class="mx-2">
                        <div slot="popover-title">确定要关闭吗</div>
                        <div slot="popover-content">
                            <el-button plain>取消</el-button>
                        </div>
                    </popover-confirm>
                    <el-button type="primary" @click="submit">提交</el-button>
                </div>
            </div>
        </drag-drawer>
        <drag-dialog :drag-dialog="dialog" @confirm="confirm">
            <reset-pwd v-if="show.resetPwd" :reset-pwd="props.resetPwd" @change-pwd-ok="changePwdOk"></reset-pwd>
            <proxy-man-config
                    v-if="show.proxyManConfig"
                    :proxy-man="props.proxyMan"
                    ref="proxyMan"
                    @successClose="successClose"
            ></proxy-man-config>
        </drag-dialog>
        <file-upload ref="fileUpload" :file-upload="fileUpload" @uploadSuccess="uploadSuccess"></file-upload>
    </div>
</template>

<script>
    import DragDrawer from '@/components/dragDrawer'
    import DragDialog from '@/components/dragDialog'
    import FileUpload from '@/components/fileUpload'
    import FormQuery from '@/components/form/query'
    import {mapState, mapActions} from 'vuex'
    import {http, apiList, constant, mixin, sweetAlert} from '@/utils'
    import {downloadFile} from '@/utils/modules/tools'
    import foxTable from '@/components/fox-table/'
    import Read from './userList/Read'
    import Modify from './userList/Modify'
    import ResetPwd from './userList/ResetPwd'
    import PopDropdown from './userList/PopDropdown'
    import PopoverConfirm from '@/components/popoverConfirm'
    import ProxyManConfig from './userList/ProxyManConfig'

    const uploadAction = () => {
        let {config: {baseUrl: {proxyURL}}} = constant
        return `${proxyURL + apiList.sys_user_import}`
    }

    export default {
        name: "UserList",
        components: {
            DragDrawer,
            DragDialog,
            FileUpload,
            FormQuery,
            foxTable,
            ResetPwd,
            PopDropdown,
            PopoverConfirm,
            ProxyManConfig
        },
        mixins: [mixin],
        data() {
            return {
                form: {
                    username: '',
                    sex: '',
                    email: '',
                    phone: '',
                    status: ''
                },
                show: {
                    batch: false,
                    resetPwd: false,
                    proxyManConfig: false,
                },
                drawer: {
                    show: false,
                    direction: 'rtl',
                    draggable: true,
                    showFooter : true,
                    data: {}
                },
                dialog: {
                    width: '300',
                    height: '300',
                    name: 'resetPwd',
                    title: '重设密码'
                },
                fileUpload: {
                    action: uploadAction()
                },
                component: {
                    type: Read,
                    ref: 'read',
                    data: {}
                },
                props: {
                    resetPwd: {},
                    proxyMan: {}
                },
                table: {
                    column: [
                        {type: 'selection', fixed: true},
                        {
                            label: '操作',
                            prop: 'oper',
                            width: '100',
                            render: (h, scope) => {
                                return (
                                    h(PopDropdown, {
                                        on: {
                                            edit: () => {
                                                this.edit(scope)
                                            },
                                            handleDetail: () => {
                                                this.handleDetail(scope)
                                            },
                                            handlePwd: () => {
                                                this.handlePwd(scope)
                                            },
                                            handleDel: () => {
                                                this.handleDel(scope)
                                            },
                                            frozen: () => {
                                                this.frozen(scope)
                                            },
                                            proxyMan: () => {
                                                this.proxyMan(scope)
                                            }
                                        }
                                    })
                                )
                            },
                        },
                        {
                            label: '用户账号',
                            prop: 'username',
                        },
                        {
                            label: '真实姓名',
                            prop: 'realname',
                        },
                        {
                            label: '头像',
                            width: '70',
                            prop: 'avatar',
                            render: (h, scope) => {
                                let imgPath = this.getAvatarView(scope)
                                debugger;
                                return (
                                    <el-image size={26} src={imgPath}
                                              preview-src-list={[imgPath]}
                                    >
                                        <div slot="error" class="cursor-pointer">
                                            <i class="el-icon-picture-outline"></i>
                                        </div>
                                    </el-image>
                                )
                            }
                        },
                        {
                            label: '性别',
                            prop: 'sex_dictText',
                            width: '70'
                        },
                        {label: '生日', prop: 'birthday', showOverflowTooltip: true},
                        {label: '手机号码', prop: 'phone', showOverflowTooltip: true},
                        {label: '邮箱', prop: 'email', showOverflowTooltip: true},
                        {label: '状态', prop: 'status_dictText', width: '70', showOverflowTooltip: true},
                    ],
                    data: [],
                    selection: [],
                    loading: false
                },
                page: {
                    currentPage: 1,
                    pageSize: 10,
                    total: 0
                }
            }
        },
        computed: {
            ...mapState({
                sex: ({dict}) => dict.sex,
                userStatus: ({dict}) => dict.userStatus,
            })
        },
        methods: {
            ...mapActions({
                getSex: 'GET_SEX',
                getUserStatus: 'GET_USER_STATUS',
                getAllRoles: 'GET_ALL_ROLES',
                getAllDepts: 'GET_ALL_DEPTS',
                getActivitiSync: 'GET_ACTIVIYI_SYNC',
            }),
            closeFlush() {
                debugger;
                this.drawer = {
                    ...this.drawer,
                    show: false,
                }
                this.queryList()
            },
            fileImport() {
                this.$modal.show('fileUpload')
            },
            async fileExport() {
                let params = {}
                let {data, filename} = await http.getFileDownload(apiList.sys_user_export, params)
                downloadFile(data, filename)

            },
            uploadSuccess() {
                this.$modal.hide('fileUpload')
                this.queryList()
            },
            getAvatarView({row: {avatar}}) {
                let {config: {baseUrl: {imgDomainURL}}} = constant
                return `${imgDomainURL}/${avatar}`
            },
            changePwdOk(row) {
                let {resetPwd: {index}} = this.props
                let {data} = this.table
                data[index] = {
                    ...row
                }
                this.table = {
                    ...this.table,
                    data
                }
                this.$modal.hide('resetPwd')
                this.show = {
                    ...this.show,
                    resetPwd: false
                }
            },
            addUser() {
                this.drawer = {
                    ...this.drawer,
                    title: '添加用户',
                    width: '500px',
                    showFooter : true,
                    show: true
                }
                this.component = {
                    ...this.component,
                    type: Modify,
                    ref: 'modify',
                    data: {}
                }
            },
            edit({row}) {
                this.drawer = {
                    ...this.drawer,
                    title: '修改用户',
                    width: '500px',
                    showFooter : true,
                    show: true
                }
                this.component = {
                    ...this.component,
                    type: Modify,
                    ref: 'modify',
                    data: row
                }
            },
            deleteBatch() {
                let {selection} = this.table
                let ids = selection.map(item => item.id).join(',')
                sweetAlert.confirm('删除', '确认要删除吗', this.confirmDeleteBatch, ids)
            },
            async confirmDeleteBatch(ids) {
                let {success, message} = await http.delete(apiList.sys_user_deleteBatch, {ids})
                if (success) {
                    sweetAlert.success(message)
                    this.queryList()
                } else {
                    sweetAlert.error(message)
                }
            },
            async frozen(scope) {
                let params = {}
                let {selection} = this.table
                if (scope) {
                    if (Number.isInteger(scope)) {
                        let ids = selection.map(item => item.id).join(',')
                        params = {
                            ...params,
                            ids,
                            status: 1                                                              //解冻
                        }
                    } else {
                        let {row: {id: ids, status}} = scope                                        //冻结单个
                        params = {
                            ...params,
                            ids,
                            status: 2
                        }
                    }

                } else {
                    let ids = selection.map(item => item.id).join(',')                                //冻结批量
                    params = {
                        ...params,
                        ids,
                        status: 2
                    }
                }
                let {success, message} = await http.put(apiList.sys_user_frozenBatch, params)
                if (success) {
                    sweetAlert.successWithTimer(message),
                        this.queryList()
                } else {
                    sweetAlert.error(message)
                }

            },
            proxyMan({row}) {
                this.dialog = {
                    ...this.dialog,
                    width: 26,
                    height: 450,
                    name: 'proxyConf',
                    title: '用户代理人设置',
                    showFooter: true
                }
                this.show = {
                    ...this.show,
                    proxyManConfig: true,
                    resetPwd : false
                }
                this.props = {
                    ...this.props,
                    proxyMan: {
                        ...row
                    }
                }
                let {name} = this.dialog
                this.$nextTick(() => {
                    this.$modal.show(name)
                })
            },
            confirm() {
                let selectUserRef = this.$refs['proxyMan']
                selectUserRef.$refs.form.validate(valid => {
                    if (valid) {
                        this.dialog = {
                            ...this.dialog,
                            loading: true
                        }
                        selectUserRef.saveData()
                    }
                })
                this.dialog = {
                    ...this.dialog,
                    loading: false
                }
            },
            submit() {
                let {ref} = this.component
                let modifyRef = this.$refs[ref]
                modifyRef.$refs.modify.validate(valid => {
                    if (valid) {
                        modifyRef.commitData()
                    }
                })
                this.dialog = {
                    ...this.dialog,
                    loading: false
                }
            },
            successClose() {
                let {name} = this.dialog
                this.$modal.hide(name)
                this.queryList()
            },
            handleDetail({row}) {
                this.drawer = {
                    ...this.drawer,
                    width: '450px',
                    title: '详情',
                    showFooter : false,
                    show: true,
                }
                this.component = {
                    ...this.component,
                    type: Read,
                    ref : 'read',
                    data: row
                }
            }
            ,
            handlePwd({row, $index: index}) {
                this.dialog = {
                    ...this.dialog,
                    width: '300',
                    height: '300',
                    name: 'resetPwd',
                    title: '重设密码',
                    showFooter: false
                }
                this.show = {
                    ...this.show,
                    resetPwd: true,
                    proxyManConfig : false,
                }
                this.props = {
                    ...this.props,
                    resetPwd: {
                        index,
                        ...row
                    }
                }
                let {name} = this.dialog
                this.$nextTick(() => {
                    this.$modal.show(name)
                })

            },
            async handleDel(scope) {
                let {row: {id}} = scope
                let {success, message, result} = await http.delete(apiList.sys_user_delete, {id})
                if (success) {
                    sweetAlert.successWithTimer('删除成功'),
                        this.queryList()
                } else {
                    sweetAlert.error(message)
                }
            },
            search() {
                this.page = {
                    ...this.page,
                    currentPage: 1
                }
                this.queryList()
            },
            reset() {
                this.$refs.form.resetFields()
            },
            checked(selection) {
                if (selection.length) {
                    this.show = {
                        ...this.show,
                        batch: true
                    }
                } else {
                    this.show = {
                        ...this.show,
                        batch: false
                    }
                }
                this.table = {
                    ...this.table,
                    selection
                }
            }
            ,
            handleCurrentChange(currentPage) {
                this.page = {
                    ...this.page,
                    currentPage
                }
                this.queryList()
            }
            ,
            handleSizeChange(pageSize) {
                this.page = {
                    ...this.page,
                    pageSize
                }
                this.queryList()
            }
            ,
            async queryList() {
                this.table = {
                    ...this.table,
                    loading: true
                }
                let {pageSize, currentPage: pageNo} = this.page
                let params = {
                    ...this.form,
                    pageSize,
                    pageNo
                }
                let {success, result: {total, records: data}} = await http.get(apiList.sys_user_query_list, params)
                if (success) {
                    this.table = {
                        ...this.table,
                        loading: false,
                        data
                    }
                    this.page = {
                        ...this.page,
                        total
                    }
                }
            }
        },
        mounted() {
            this.queryList()
            this.getSex()
            this.getUserStatus()
            this.getAllRoles()
            this.getAllDepts()
            this.getActivitiSync()
        }
    }
</script>

